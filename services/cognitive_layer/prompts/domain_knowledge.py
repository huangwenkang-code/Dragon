SENTIMENT_ANALYSIS_RULES = """
## 情绪分析维度

### 六维情绪评估
1. **sentiment_score** (-1到1): 整体情绪方向
   - +0.8以上: 极度乐观(警惕过度一致)
   - +0.3到+0.8: 偏乐观
   - -0.3到+0.3: 中性
   - -0.8到-0.3: 偏悲观
   - -0.8以下: 极度悲观(可能超跌反弹)

2. **narrative_score** (0到1): 叙事强度
   - 政策叙事>产业叙事>公司叙事>交易叙事
   - 新叙事>旧叙事(新颖性加分)
   - 越具体越好(有数字、有标的、有逻辑链)

3. **hype_score** (0到1): 炒作热度
   - 龙虎榜游资抱团: +0.3
   - 社交媒体热搜: +0.2
   - 连板高度>3板: +0.2
   - 涨停家数>10: +0.2
   - 板块集体异动: +0.1

4. **consistency_score** (0到1): 多源一致性
   - 所有来源指向同一方向: 0.8+
   - 多数来源一致但有个别分歧: 0.5-0.7
   - 来源分歧明显: <0.4

5. **risk_score** (0到1): 风险信号
   - 题材退潮信号(炸板/高位回落): +0.3
   - 利好出尽/预期兑现: +0.2
   - 监管干预/问询函: +0.3
   - 大盘系统性风险: +0.2

6. **diffusion_speed** (0到1): 扩散速度
   - 消息在多个平台同时出现: 高
   - 龙头带动板块跟涨: 扩散中
   - 仅单一来源: 低

### 中国市场特有的情绪信号
- "利好出尽是利空": 前期炒作充分的题材, 正式利好落地后反而下跌
- "利空出尽是利好": 预期的坏消息确认后, 市场反而上涨
- "见光死": 利好公布即卖出的现象
- "情绪高标": 市场情绪集中的龙头股, 具有风向标作用
"""

