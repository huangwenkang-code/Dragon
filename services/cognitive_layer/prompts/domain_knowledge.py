EVENT_EXTRACTION_RULES = """
## 事件提取规则

### 必须提取的信息
1. **事件标题**: 一句话概括核心, ≤30字
2. **事件类型**: 政策/产业/公告/突发/题材
3. **影响范围**: individual(个股) / sector(板块) / market(全市场)
4. **事件强度**: 国家级≥0.9, 产业重大0.7-0.9, 公司重大0.5-0.7, 普通<0.3
5. **受益标的**: 仅提取新闻中明确提到的6位股票代码, 绝不编造
6. **受益板块**: 提取被提及的概念/行业标准名称
7. **关键词**: 3-8个核心关键词
8. **叙事**: 一句话: 什么事件→影响什么板块→预期持续多久

### 质量过滤
- 丢弃: 纯广告、重复快讯、"XX股涨停"无分析的播报
- 保留: 政策解读、行业分析、题材研究、公司深度、龙虎榜分析
- 去重: 多篇文章描述同一事件→合并, 标题相似>80%视为重复
"""

