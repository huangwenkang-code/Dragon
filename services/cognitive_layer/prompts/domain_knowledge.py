A_SHARE_MARKET_STRUCTURE = """
## A股题材驱动逻辑

- 政策驱动: 国务院/部委文件 → 产业政策利好 → 相关概念板块集体异动
- 产业驱动: 技术突破/产品发布/涨价 → 龙头先涨 → 板块内扩散
- 资金驱动: 游资抱团 → 龙虎榜联动 → 情绪高标形成 → 跟风效应
- 事件驱动: 突发事件 → 受益标的快速反应 → 持续性取决于事件等级

## 概念轮动规律

- 强势市场: 主线概念持续2-4周, 龙头连板3-8板, 板块内有梯队接力
- 弱势市场: 概念一日游, 无持续性, 资金快速轮动
- 概念强度: 涨停家数>5强, 2-5中等, <2弱
- 龙头判定: 最先涨停 + 封单最大 + 带动板块跟涨

## 事件影响级别

| 级别 | 类型 | 示例 | 持续性 |
|------|------|------|--------|
| 国家级 | 国务院政策/领导人讲话 | 国务院印发产业规划 | 2-4周 |
| 产业级 | 技术突破/产能释放 | 华为发布鸿蒙PC | 1-2周 |
| 公司级 | 重大合同/业绩预告 | 宁德时代获大额订单 | 3-5天 |
| 交易级 | 龙虎榜/大宗交易 | 机构席位买入超1亿 | 1-3天 |
| 情绪级 | 社交媒体传播 | 雪球热搜/股吧热议 | 1天 |
"""

